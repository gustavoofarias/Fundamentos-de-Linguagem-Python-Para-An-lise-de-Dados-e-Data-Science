'''
Exercício Python 006:
 Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
'''

entrada = int(input("Digite um número:"))

dobro = entrada * 2
triplo =  entrada * 3
raiz = entrada  ** 0.5

print(f"O número é {entrada}")
print(f"O dobro é {dobro}")
print(f"Seu triplo {triplo}")
print(f"Sua raiz {raiz}")

